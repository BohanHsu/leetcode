require 'minitest/autorun'
require '../subset_ii'

describe 'subsets_with_dup(nums)' do
  it 'should work' do
    nums = [1,2,2]
    expected_result = [
      [2],
      [1],
      [1,2,2],
      [2,2],
      [1,2],
      []
    ].sort
    subsets_with_dup(nums).sort.must_equal(expected_result)
  end

  it 'should work 1' do
    nums = [1,9,8,3,-1,0]
    expected_result = [[],[-1],[-1,0],[-1,0,1],[-1,0,1,3],[-1,0,1,3,8],
                       [-1,0,1,3,8,9],[-1,0,1,3,9],
                       [-1,0,1,8],[-1,0,1,8,9],[-1,0,1,9],[-1,0,3],[-1,0,3,8],[-1,0,3,8,9],
                       [-1,0,3,9],[-1,0,8],[-1,0,8,9],[-1,0,9],[-1,1],[-1,1,3],[-1,1,3,8],
                       [-1,1,3,8,9],[-1,1,3,9],[-1,1,8],[-1,1,8,9],[-1,1,9],[-1,3],[-1,3,8],
                       [-1,3,8,9],[-1,3,9],[-1,8],[-1,8,9],[-1,9],[0],[0,1],[0,1,3],[0,1,3,8],
                       [0,1,3,8,9],[0,1,3,9],[0,1,8],[0,1,8,9],[0,1,9],[0,3],[0,3,8],[0,3,8,9],
                       [0,3,9],[0,8],[0,8,9],[0,9],[1],[1,3],[1,3,8],[1,3,8,9],[1,3,9],[1,8],
                       [1,8,9],[1,9],[3],[3,8],[3,8,9],[3,9],[8],[8,9],[9]]

    subsets_with_dup(nums).sort.must_equal(expected_result)
  end
end
